# https://leetcode.com/problems/reverse-string/

# string given as list of chars
# reverse in-place, allocating no extra memory


class Solution:
    def reverseString(self, s: List[str]) -> None:
        """
        Do not return anything, modify s in-place instead.
        """
        i, j = 0, len(s) - 1
        while i < j:
            s[i], s[j] = s[j], s[i]
            i += 1
            j -= 1
